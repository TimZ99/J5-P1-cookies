<h2>Nederlandse cookie wetgeving</h2>
<p style="text-align: justify;">
Sinds 2012 is Artikel 11.7a van de Telecommunicatiewet van kracht. Artikel 11.7a van de Telecommunicatiewet beter bekend als de cookiewet houd in dat elke website die bezoekers vanuit Nederland trekt de bezoeker moet informeren over welke cookies er worden geplaatst op zijn of haar computer en waar deze voor dienen. Dit kan via een privacystatementpagina. Naast deze informatie moet elke bezoeker vanuit Nederland éénmalig toestemming geven voor het plaatsen van cookies. Deze toestemming kan gevraagd worden via een pop-up of een balk boven aan de pagina met een “Ik geef toestemming” knop.
<br>
Op 11 maart 2015 heeft de Nederlandse regering de Telecommunicatiewet aangepast. Websites hebben nu geen toestemming meer nodig voor het plaatsen van analytische cookies.
Analytische cookies zijn cookies die bijvoorbeeld worden gebruikt om bezoekersaantallen bij te houden. Deze cookies hebben nauwelijks gevolgen voor de privacy.
<br>
Naast analytische cookies hoef je voor functionele cookies ook geen toestemming te vragen. Onder functionele cookies vallen cookies die onder andere worden gebruik om bij te houden wat er in een winkelmandje zit. Dit zijn heel vaak session cookies. Na een bepaalde tijd worden deze verwijderd. Deze cookies zijn nodig om de dienst te laten werken.
Tracking cookies, deze cookies vallen in de categorie; toestemming vereist. Met deze cookies word inbreuk op je privacy gedaan. Deze cookies worden onder andere gebruikt om gerichte advertenties te laten zien aan je. Er word een profiel gemaakt (profiling).
</p>
