<h2>Geschiedenis</h2>
<p style="text-align: justify;">
Cookies werden voor het eerst geïntroduceerd in de browser Netscape 1.0. Een andere browser, Internet Explorer ondersteunde cookies pas vanaf versie 2.0. Pas in Netscape 3 liet Lou Montulli een optie inbouwen waardoor internetgebruikers de cookies konden zien en waarmee je gewaarschuwd kon worden als er cookies geplaatst probeerde te worden. In de tussentijd lag de advertentie industrie ook niet onder een steen en begonnen ze cookies te gebruiken om een profiel van iemand te maken.
<br>
In 1997 kwam er een voorstel dat browsermakers aanmoedigde om het gebruik van cookies overzichtelijk te maken. Je kon hierdoor instellen in welke mate je cookies wil accepteren. Hiermee kan bijvoorbeeld profilering tegen worden gegaan. </p>
